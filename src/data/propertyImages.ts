// Image preloading for demo performance
export const preloadPropertyImages = async (propertyIds: string[]): Promise<void> => {
  const imageUrls: string[] = [];
  
  propertyIds.forEach(propertyId => {
    const images = propertyImages[propertyId];
    if (images) {
      imageUrls.push(images.primary, images.thumbnail, ...images.gallery);
    }
  });

  // Preload all images
  const preloadPromises = imageUrls.map(url => {
    return new Promise<void>((resolve, reject) => {
      const img = new Image();
      img.onload = () => resolve();
      img.onerror = () => reject(new Error(`Failed to load image: ${url}`));
      img.src = url;
    });
  });

  try {
    await Promise.all(preloadPromises);
    console.log('✅ All property images preloaded successfully');
  } catch (error) {
    console.warn('⚠️ Some property images failed to preload:', error);
  }
};

// Image optimization utilities
export const getOptimizedImageUrl = (
  baseUrl: string, 
  width: number, 
  height?: number, 
  quality: number = 80
): string => {
  if (!baseUrl.includes('unsplash.com')) {
    return baseUrl;
  }
  
  const url = new URL(baseUrl);
  url.searchParams.set('w', width.toString());
  if (height) {
    url.searchParams.set('h', height.toString());
  }
  url.searchParams.set('q', quality.toString());
  url.searchParams.set('fit', 'crop');
  
  return url.toString();
};

